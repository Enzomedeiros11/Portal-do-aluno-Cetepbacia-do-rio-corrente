-- 1. TABELA DE USUÁRIOS (Perfis: Alunos, Professores, Coordenação)
CREATE TABLE IF NOT EXISTS public.usuarios (
    id UUID PRIMARY KEY,
    nome TEXT NOT NULL,
    email TEXT UNIQUE NOT NULL,
    tipo TEXT DEFAULT 'student', -- 'student', 'teacher', 'admin'
    curso TEXT DEFAULT 'Técnico em Informática',
    grade TEXT DEFAULT '1º Ano',
    avatar_url TEXT,
    frequencia INTEGER DEFAULT 100,
    notas JSONB DEFAULT '{}'::jsonb,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. TABELA DE MENSAGENS (Mural e Chat)
CREATE TABLE IF NOT EXISTS public.mensagens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    texto TEXT,
    usuario_nome TEXT NOT NULL,
    usuario_email TEXT NOT NULL,
    turma_id TEXT NOT NULL,
    role TEXT DEFAULT 'student',
    arquivo_url TEXT,
    arquivo_nome TEXT,
    arquivo_tipo TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 3. TABELA DE NOTAS (Opcional - Se preferir separar por disciplina)
CREATE TABLE IF NOT EXISTS public.notas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    aluno_id UUID REFERENCES public.usuarios(id),
    disciplina TEXT NOT NULL,
    nota DECIMAL DEFAULT 0,
    bimestre INTEGER NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 4. TABELA DE FREQUENCIA (Opcional - Histórico detalhado)
CREATE TABLE IF NOT EXISTS public.frequencia (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    aluno_id UUID REFERENCES public.usuarios(id),
    data DATE DEFAULT CURRENT_DATE,
    presente BOOLEAN DEFAULT true,
    disciplina TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 5. TABELA DE ARQUIVOS (Metadados dos uploads)
CREATE TABLE IF NOT EXISTS public.arquivos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome TEXT NOT NULL,
    url TEXT NOT NULL,
    tipo TEXT,
    tamanho BIGINT,
    owner_id UUID REFERENCES public.usuarios(id),
    turma_id TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 6. HABILITAR SEGURANÇA (RLS)
ALTER TABLE public.usuarios ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.mensagens ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.notas ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.frequencia ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.arquivos ENABLE ROW LEVEL SECURITY;

-- 7. POLÍTICAS DE ACESSO (Simplificadas para o Portal)
-- Usuarios
DROP POLICY IF EXISTS "Enable read access for authenticated" ON public.usuarios;
CREATE POLICY "Enable read access for authenticated" ON public.usuarios FOR SELECT TO authenticated USING (true);
DROP POLICY IF EXISTS "Enable update for own record" ON public.usuarios;
CREATE POLICY "Enable update for own record" ON public.usuarios FOR UPDATE TO authenticated USING (auth.uid() = id);
DROP POLICY IF EXISTS "Enable insert for authenticated" ON public.usuarios;
CREATE POLICY "Enable insert for authenticated" ON public.usuarios FOR INSERT TO authenticated WITH CHECK (true);

-- Mensagens
DROP POLICY IF EXISTS "Mensagens access" ON public.mensagens;
CREATE POLICY "Mensagens access" ON public.mensagens FOR SELECT TO authenticated USING (true);
DROP POLICY IF EXISTS "Mensagens insert" ON public.mensagens;
CREATE POLICY "Mensagens insert" ON public.mensagens FOR INSERT TO authenticated WITH CHECK (true);

-- Notas
DROP POLICY IF EXISTS "Notas access" ON public.notas;
CREATE POLICY "Notas access" ON public.notas FOR SELECT TO authenticated USING (true);
DROP POLICY IF EXISTS "Notas insert/update" ON public.notas;
CREATE POLICY "Notas insert/update" ON public.notas FOR ALL TO authenticated USING (true);

